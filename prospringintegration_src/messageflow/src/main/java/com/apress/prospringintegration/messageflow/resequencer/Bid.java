package com.apress.prospringintegration.messageflow.resequencer;

import java.util.Date;

public class Bid {
    Date time;
    int order;

    public Bid() {
    }

    public Bid(Date time, int order) {
        this.time = time;
        this.order = order;
    }

    public Date getTime() {
        return time;
    }

    public void setTime(Date time) {
        this.time = time;
    }

    public int getOrder() {
        return order;
    }

    public void setOrder(int order) {
        this.order = order;
    }
}
