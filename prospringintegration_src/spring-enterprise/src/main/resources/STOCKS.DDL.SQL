create table STOCKS (
  ID SERIAL,
  INVENTORY_CODE VARCHAR(25) NOT NULL,
  SYMBOL VARCHAR(4),
  EXCHANGE_ID VARCHAR(10),
  PRICE_PER_SHARE DECIMAL (6,2) NOT NULL,
  QUANTITY_AVAILABLE INTEGER NOT NULL,
  PURCHASE_DATE DATE NOT NULL,
  CONSTRAINT PK_STOCKID PRIMARY KEY (ID),
  CONSTRAINT INV_CODE UNIQUE(INVENTORY_CODE)
);

create table ACCOUNTS (
  ID SERIAL,
  CUSTOMER_NAME VARCHAR(25) NOT NULL,
  AVAILABLE_FUNDS DECIMAL(9,2) NOT NULL,
  CONSTRAINT PK_ACCOUNTID PRIMARY KEY (ID)
);

create table HOLDINGS (
  ID SERIAL,
  ACCOUNT_ID INTEGER NOT NULL,
  STOCK INTEGER NOT NULL,
  PURCHASE_PRICE DECIMAL(6,2) NOT NULL,
  PURCHASE_TIME DATE,
  QUANTITY INTEGER NOT NULL,
  CONSTRAINT PK_HOLDINGID PRIMARY KEY (ID),
  CONSTRAINT FK_ACCOUNTID FOREIGN KEY (ACCOUNT_ID) REFERENCES ACCOUNTS(ID)
);

